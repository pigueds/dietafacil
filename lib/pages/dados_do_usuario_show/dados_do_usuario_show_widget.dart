              ? (tdeeINTER * 0.8).round().toDouble()
              : (objetivo == 'Ganho de massa')
                  ? (tdeeINTER + 500).round().toDouble()
                  : tdeeINTER.round().toDouble();
        }(FFAppState().stateTDEEinter, FFAppState().stateOBJETIVO);
        safeSetState(() {});
      }
      FFAppState().gproteinadiaria =
          FFAppState().statePeso * FFAppState().gproteinaporkg;
      safeSetState(() {});
      FFAppState().ggorduradiaria =
          FFAppState().statePeso * FFAppState().ggorduraporkg;
      safeSetState(() {});
      FFAppState().gcarbodiario = (FFAppState().stateTDEE -
              (FFAppState().gproteinadiaria * 4) -
              (FFAppState().ggorduradiaria * 9)) /
          4;
      safeSetState(() {});
      FFAppState().IMC = FFAppState().statePeso /
          (FFAppState().stateAltura * FFAppState().stateAltura);
      safeSetState(() {});
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => safeSetState(() {}));
  }

